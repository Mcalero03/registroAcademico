<template>
  <div data-app>
    <v-card class="p-3 mt-3">
      <v-container>
        <h2>
          {{ title }}
        </h2>
        <div class="options-table">
          <base-button
            type="primary"
            title="Agregar"
            @click="addRecord()"
          ></base-button>
          <v-col cols="12" sm="4" lg="4" xl="4" class="pl-0 pb-0 pr-0">
            <v-text-field
              class="mt-3"
              variant="outlined"
              label="Buscar"
              type="text"
              v-model="search"
            >
            </v-text-field>
          </v-col>
        </div>
      </v-container>
      <v-data-table-server
        :headers="headers"
        :items-length="total"
        :items="records"
        :loading="loading"
        items-title="id"
        item-value="id"
        @update:options="getDataFromApi"
      >
        <template v-slot:[`item.actions`]="{ item }">
          <v-icon
            size="20"
            class="mr-2"
            @click="editItem(item.raw)"
            icon="mdi-pencil"
          />
          <v-icon
            size="20"
            class="mr-2"
            @click="deleteItem(item.raw)"
            icon="mdi-delete"
          />
        </template>
        <template v-slot:no-data>
          <v-icon @click="initialize" icon="mdi-refresh" />
        </template>
      </v-data-table-server>
    </v-card>

    <v-dialog v-model="dialog" max-width="800px" persistent>
      <v-card>
        <v-card-title>
          <h2 class="mx-auto pt-3 mb-3 text-center black-secondary">
            {{ formTitle }}
          </h2>
        </v-card-title>
        <v-card-text class="pt-0">
          <v-container>
            <!-- Form -->
            <v-row class="pt-0">
              <!-- cycle_number  -->
              <v-col cols="6" sm="3" md="3">
                <base-input
                  label="Número de ciclo"
                  v-model="v$.editedItem.cycle_number.$model"
                  :rules="v$.editedItem.cycle_number"
                  type="number"
                  min="1"
                />
              </v-col>
              <!-- cycle_number  -->
              <!-- year  -->
              <v-col cols="6" sm="3" md="3">
                <base-input
                  label="Año lectivo"
                  v-model="v$.editedItem.year.$model"
                  :rules="v$.editedItem.year"
                  type="number"
                  min="1900"
                  max="2099"
                />
              </v-col>
              <!-- year  -->
              <!-- start_date  -->
              <v-col cols="12" sm="6" md="6">
                <base-input
                  label="Fecha de inicio"
                  v-model="v$.editedItem.start_date.$model"
                  :rules="v$.editedItem.start_date"
                  type="date"
                />
              </v-col>
              <!-- start_date  -->
              <!-- end_date  -->
              <v-col cols="12" sm="6" md="6">
                <base-input
                  label="Fecha de finalización"
                  v-model="v$.editedItem.end_date.$model"
                  :rules="v$.editedItem.end_date"
                  type="date"
                />
              </v-col>
              <!-- end_date  -->
              <!-- status  -->
              <v-col cols="12" sm="6" md="6">
                <base-select
                  label="Estado del ciclo"
                  :items="status"
                  item-title="status"
                  item-value="status"
                  v-model="v$.editedItem.status.$model"
                  :rules="v$.editedItem.status"
                />
              </v-col>
              <!-- status  -->
            </v-row>
            <!-- Subject table -->
            <v-row>
              <v-col align="center" cols="12" md="12" sm="12" class="pt-4">
                <div class="table-responsive-md">
                  <v-table>
                    <thead>
                      <tr>
                        <th>MATERIA</th>
                        <th>AGREGAR</th>
                      </tr>
                    </thead>
                    <tbody>
                      <tr
                        v-for="(subject, index) in editedItem.subjects"
                        v-bind:index="index"
                        :key="index"
                      >
                        <td v-text="subject.subject_name"></td>
                        <td>
                          <v-checkbox
                            v-model="subject.subject_status"
                            true-value="1"
                            false-value="0"
                            class="ml-6"
                            color="info"
                            disabled
                            v-if="editedIndex != -1"
                          />
                          <v-checkbox
                            v-model="subject.subject_status"
                            true-value="1"
                            false-value="0"
                            class="ml-6"
                            color="info"
                            v-else-if="editedIndex == -1"
                          />
                        </td>
                      </tr>
                      <tr v-if="editedItem.subjects.length == 0">
                        <td colspan="5" class="text-center pt-3">
                          <loader v-if="loading" />
                          <p>No se han encontrado materias para este ciclo</p>
                        </td>
                      </tr>
                    </tbody>
                  </v-table>
                </div>
              </v-col>
            </v-row>
            <!-- Subject table -->
            <!-- Form -->
            <v-row>
              <v-col align="center">
                <base-button type="primary" title="Guardar" @click="save" />
                <base-button
                  class="ms-1"
                  type="secondary"
                  title="Cancelar"
                  @click="close"
                />
              </v-col>
            </v-row>
          </v-container>
        </v-card-text>
      </v-card>
    </v-dialog>

    <v-dialog v-model="dialogDelete" max-width="400px">
      <v-card class="h-100">
        <v-container>
          <h1 class="black-secondary text-center mt-3 mb-3">
            Eliminar registro
          </h1>
          <v-row>
            <v-col align="center">
              <base-button
                type="primary"
                title="Confirmar"
                @click="deleteItemConfirm"
              />
              <base-button
                class="ms-1"
                type="secondary"
                title="Cancelar"
                @click="closeDelete"
              />
            </v-col>
          </v-row>
        </v-container>
      </v-card>
    </v-dialog>
  </div>
</template> 



<script>
import { useVuelidate } from "@vuelidate/core";
import { messages } from "@/utils/validators/i18n-validators";
import { helpers, minLength, required, maxLength } from "@vuelidate/validators";

import cycleApi from "@/services/cycleApi";
import subjectApi from "@/services/subjectApi";
import BaseButton from "../components/base-components/BaseButton.vue";
import BaseInput from "../components/base-components/BaseInput.vue";
import BaseSelect from "../components/base-components/BaseSelect.vue";
import Loader from "@/components/Loader.vue";

import useAlert from "../composables/useAlert";

const { alert } = useAlert();
const langMessages = messages["es"].validations;

export default {
  components: { BaseButton, BaseInput, BaseSelect, Loader },

  setup() {
    return { v$: useVuelidate() };
  },

  data() {
    return {
      search: "",
      dialog: false,
      dialogDelete: false,
      editedIndex: -1,
      title: "CICLO",
      headers: [
        { title: "CICLO", key: "cycle_number" },
        { title: "AÑO", key: "year" },
        { title: "INICIO", key: "start_date" },
        { title: "FIN", key: "end_date" },
        { title: "ESTADO", key: "status" },
        { title: "ACCIONES", key: "actions", sortable: false },
      ],
      total: 0,
      records: [],
      loading: false,
      debounce: 0,
      status: ["Abierto", "En proceso", "Finalizado"],
      options: {},
      editedItem: {
        cycle_number: "",
        year: "",
        start_date: "",
        end_date: "",
        status: "",
        subjects: [],
      },
      defaultItem: {
        cycle_number: "",
        year: "",
        start_date: "",
        end_date: "",
        status: "",
        subjects: [],
      },
    };
  },

  mounted() {
    this.initialize();
  },
  computed: {
    formTitle() {
      return this.editedIndex === -1 ? "Nuevo registro" : "Editar registro";
    },
  },

  watch: {
    search(val) {
      this.getDataFromApi();
    },
    dialog(val) {
      val || this.close();
    },
    dialogDelete(val) {
      val || this.closeDelete();
    },
  },

  validations() {
    return {
      editedItem: {
        cycle_number: {
          required: helpers.withMessage(langMessages.required, required),
          minLength: helpers.withMessage(
            ({ $params }) => langMessages.minLength($params),
            minLength(1)
          ),
          maxLength: (({ $params }) => maxLength($params), maxLength(1)),
        },
        year: {
          required: helpers.withMessage(langMessages.required, required),
          minLength: helpers.withMessage(
            ({ $params }) => langMessages.minLength($params),
            minLength(4)
          ),
          maxLength: (({ $params }) => maxLength($params), maxLength(4)),
        },
        start_date: {
          required: helpers.withMessage(langMessages.required, required),
        },
        end_date: {
          required: helpers.withMessage(langMessages.required, required),
        },
        status: {
          required: helpers.withMessage(langMessages.required, required),
          minLength: helpers.withMessage(
            ({ $params }) => langMessages.minLength($params),
            minLength(4)
          ),
        },
      },
    };
  },

  methods: {
    async initialize() {
      this.loading = true;
      this.records = [];

      let requests = [
        this.getDataFromApi(),
        subjectApi.get(null, {
          params: {
            itemsPerPage: -1,
          },
        }),
      ];
      const responses = await Promise.all(requests).catch((error) => {
        alert.error("No fue posible obtener el registro.");
      });

      if (responses) {
        this.editedItem.subjects = responses[1].data.cycleSubject;
        console.log(this.editedItem.subjects);
      }

      this.loading = false;
    },

    getDataFromApi(options) {
      this.loading = false;
      this.records = [];

      clearTimeout(this.debounce);
      this.debounce = setTimeout(async () => {
        try {
          const { data } = await cycleApi.get(null, {
            params: { ...options, search: this.search },
          });

          this.records = data.data;
          this.total = data.total;
          this.loading = false;
        } catch (error) {
          alert.error("No fue posible obtener los registros.");
        }
      });
    },

    created() {
      this.initialize();
    },

    beforeMount() {
      this.getDataFromApi({
        page: 1,
        itemsPerPage: 10,
        sortBy: [],
        search: "",
      });
    },

    close() {
      this.dialog = false;
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      });
    },

    addRecord() {
      this.dialog = true;
      this.editedIndex = -1;
      this.editedItem = Object.assign({}, this.defaultItem);
      this.v$.$reset();
    },

    editItem(item) {
      this.editedIndex = this.records.indexOf(item);
      this.editedItem = Object.assign({}, item);
      this.dialog = true;
    },

    async save() {
      this.v$.$validate();
      if (this.v$.$invalid) {
        alert.error("Campo obligatorio");
        return;
      }

      // Updating record
      if (this.editedIndex > -1) {
        const edited = Object.assign(
          this.records[this.editedIndex],
          this.editedItem
        );

        try {
          const { data } = await cycleApi.put(`/${edited.id}`, edited);
          alert.success(data.message);
        } catch (error) {
          alert.error("No fue posible actualizar el registro.");
        }

        this.close();
        this.initialize();
        return;
      }

      // Creating record
      try {
        const { data } = await cycleApi.post(null, this.editedItem);
        alert.success(data.message);
      } catch (error) {
        alert.error("No fue posible crear el registro.");
      }

      this.close();
      this.initialize();
      return;
    },

    deleteItem(item) {
      this.editedIndex = this.records.indexOf(item);
      this.editedItem = Object.assign({}, item);

      this.dialogDelete = true;
    },

    async deleteItemConfirm() {
      try {
        const { data } = await cycleApi.delete(`/${this.editedItem.id}`, {
          params: { id: this.editedItem.id },
        });

        alert.success(data.message);
      } catch (error) {
        this.close();
      }
      this.initialize();
      this.closeDelete();
    },

    closeDelete() {
      this.dialogDelete = false;
      this.$nextTick(() => {
        this.editedItem = Object.assign({}, this.defaultItem);
        this.editedIndex = -1;
      });
    },
  },
};
</script>